    private void DisplayStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplayStaffActionPerformed
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    Staff staff = new Staff(); // Create a Staff object using the default constructor
    ResultSet result = staff.getInfo();
    
    if (result != null) {
        try {
            // Clear existing rows in the table model
            model.setRowCount(0);
            
            String sId, sName, sPosition, sContactInfo, sAddress, sDob;
            while (result.next()) {
                sId = result.getString("staffID");
                sName = result.getString("staffName");
                sPosition = result.getString("staffPosition");
                sContactInfo = result.getString("staffContactInformation");
                sAddress = result.getString("staffAddress");
                sDob = result.getString("staffDateOfBirth");
                String[] arr = {sId, sName, sPosition, sContactInfo, sAddress, sDob};
                model.addRow(arr);
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    
    }      }//GEN-LAST:event_DisplayStaffActionPerformed

