    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // Retrieve data from text field
        int staffID = Integer.parseInt(staffIDtfield.getText().trim());

        // Create a new Staff object
        Staff staff = new Staff();

        // Call searchStaffMember method to find staff information
        staff.search(staffID);

        // Populate text fields with retrieved staff information
        staffINametxtfield.setText(staff.getPersonName());
        staffPositiontxtfield.setText(staff.getStaffPosition());
        staffCInfotxtfield.setText(staff.getContactInfo());
        staffAddresstxtfield.setText(staff.getPersonAddress());
        staffDOBtxtfield.setText(staff.getPersonDob());
        // Update other fields if necessary
    }//GEN-LAST:event_searchActionPerformed

